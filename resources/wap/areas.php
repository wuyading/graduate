                <h3 class="title">铜仁</h3>
                <img src="/mobile/images/tongren.jpg" />
                <p>铜仁市，贵州省辖地级市，有“中国西部名城”之称。位于贵州省东北部，武陵山区腹地，东邻湖南省怀化市，北与重庆市接壤，是连接中南地区与西南边陲的纽带，享有“黔东门户”之美誉。</p>
                <p>全市辖碧江区、万山区、江口县、石阡县、思南县、德江县、松桃苗族自治县、玉屏侗族自治县、印江土家族苗族自治县、沿河土家族自治县、大龙开发区、铜仁高新技术
                    产业开发区，截至2014年4月，国土面积1.8万平方公里，总人口427万，聚居着土家、汉、苗、侗、仡佬等29个民族，少数民族占总人口的70.45%。</p>
                <p>铜仁是社会稳定风险评估发源地，是首批国家智慧城市试点市，是武陵山片区区域发展与扶贫攻坚示范区，是民族团结进步示范区。</p>
            </div>
            <div class="d-environment">
                <h3 class="d-title">建制沿革</h3>
                <p>铜仁历史悠久，可以追溯到新石器时代。<br>
                    铜仁市各区县<br>
                    秦代为黔中郡腹部地区。<br>
                    汉时改隶武陵郡，蜀汉时始有县治。<br>
                    唐置万安县，后改常丰县，分属思州、锦州、黔州。<br>
                    宋末元初设思州、思南两宣慰司。</p>
                <p>铜仁原名铜人，相传元朝时有渔人在铜岩处潜入江底，得铜人三尊。元代设置“铜人大小江蛮夷军民长官司”。隶属思南宣慰司。明永乐十一年撤思州、思南宣慰司，于今
                    境地设铜仁、思南、石阡、乌罗4府，均隶属于由此而设置的贵州布政使司。明正统三年（公元1438年）废乌罗府，其大部并入铜仁府。铜仁由此得名。<br>
                    清代铜仁建置无变化。</p>
                <p>民国元年（公元1912年）撤铜仁县（治所今江口县）并入铜仁府（治所今铜仁市）。</p>
                <p>民国2年（1913年）改铜仁府为铜仁县（治所今铜仁市）；同时，改思南府为思南县，改石阡府为石阡县，改松桃直隶厅为松桃县。另外，恢复原铜仁县（治所今江口
                    县）改名江口县，安化县改名德江县，以原思南府沿河佑溪吏目驻地设沿河县，以原铜仁府省溪吏目设省溪县（今万山特区）。玉屏县、印江县建置不变。</p>
                <p>民国3年（1914年）以原铜仁县正大营县丞驻地设正大营分县，隶属松桃县兼管；以原四十八溪主薄驻地设四十八溪（今甘龙口）分县，隶属松挑县兼管。</p>
                <p>民国24年（公元1935年）6月，经国民政府军事委员会委员长行营核准，在贵州省建立11个行政督察区。其中第六行政督察区专员公署驻思南，辖思南、德江、印江、沿
                    河、石阡等县，第五行政督察区专员公署驻铜仁辖铜仁、江口、松挑、玉屏、省溪等县。</p>
                <p>民国25年（1936年）1月，正大营分县并入铜仁县，甘龙口分县并入松桃县。同年3月，全省缩编为8个督察区，第六督察区专员公署驻铜仁，辖铜仁、江口、松桃、玉屏、
                    石歼、省溪、思南、德江、印江、沿河、后坪（治所今沿河县后坪乡）13县。</p>
                <p>民国26年（1937年）11月，督察区重新设置，德江、后坪2县划属第五督察区（治所今遵义市），其余9县隶属第一督察区（治所今镇远县）。</p>
                <p>民国30年（1941年）撤省溪县，辖地分别并入铜仁、玉屏2县；撒后坪县，辖地分别并入沿河、务川2县。</p>
                <p>民国32年（1943年）2月，全省改设6个行政督察区。第六督察区专员公署驻铜仁，辖铜仁、江口、玉屏、松桃、印江、石阡、思南、德江、沿河9县。</p>
                <p>1950年1月12日，铜仁地区全境解放，当年设铜仁专区，专员公署驻铜仁县，辖铜仁、玉屏、松挑、江口、印江、石阡、思南、德江、沿河9县。</p>
                <p>1956年9月撤销松桃县，设立松桃苗族自治县。1958年12月撤销玉屏、江口二县，并入铜仁县。1961年8月恢复玉屏、江口两县。</p>
                <p>1966年12月设立万山特区，1968年9月撤销，1970年8月恢复万山特区。</p>
                <p>1979年1月撤销铜仁地区革命委员会正式设立铜仁地区行政公署（筒称行署），作为贵州省人民政府行政派出机关的现行建制。</p>
                <p>1983年9月7日国务院批准撤销玉屏县建立玉屏侗族自治县，原行政区域不变；1986年10月7日国务院批准撤销沿河县，设立沿河土家族自治县，原行政区域不变；1986
                    年12月13日，国务院批准撤销印江县，设立印江土家族苗族自治县，原行政区域不变。1987年8月21日国务院批准撒销铜仁县，设立铜仁市，原行政区域不变。</p>
                <p>1984年1月将人民公社改为乡，通过建乡建镇调整，全区乡级行政区划分60个区。</p>
                <p>1987年新建民族乡79个，乡级建制为60个区，10个区级镇，区（镇）下辖315个乡、22个乡级镇，83个民族乡。</p>
                <p>1991年全区通过“建并撤”并报经省人民政府批复，自此开始建置为39个乡、56个民族乡、69个镇、4个街道办事处。</p>
                <p>2011年10月22日，撤消铜仁地区设地级铜仁市，撤消原=县级铜仁市设碧江区，以原县级铜仁市行政区域为新的碧江区行政区域；撤消万山特区，设铜仁市万山区。</p>
