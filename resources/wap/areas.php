                <h3 class="d-title">地理环境</h3>
                <p>苏州位于江苏省南部，东临上海，南接浙江，西抱太湖，北依长江。苏州市区中心地理坐标为北纬31°19’，东经120°37’。</p>
                <p class="paragraph">地貌</p>
                <p>全市地势低平，平原占总面积的55%，苏州分别隶属于两个一级的自然地理区：长江三角洲平原地区和太湖平原地区，分属于4个二级自然区：沿江平原沙洲区、苏锡平原
                    区、太湖及湖滨丘陵区、阳澄淀泖低地区。地貌特征以平缓平原为上，全市的地势低平，自西向东缓慢倾斜，平原的海拔高度3～4米，阳澄湖和吴江一带仅2米左右。</p>
                <p>低山丘陵零星散布，一般高100～350米，分布在西部山区和太湖诸岛，其中以穹窿山最高（342米），还有南阳山（338米）、西洞庭山缥缈峰（336米）、东洞庭山莫
                    里峰（293米）、七子山（294米）、天平山（201米）、灵岩山（182米）、渔洋山（171米）、虞山（262米）、潭山（252米）等。</p>
