                <p class="paragraph">水文</p>
                <p>苏州古城境内河港交错，湖荡密布，最著名的湖泊有位于西隅的太湖和漕湖；东有淀山湖、澄湖；北有昆承湖；中有阳澄湖、金鸡湖、独墅湖；长江及京杭运河贯穿市区之
                    北。太湖水量北泄入江和东进淀泖后，经黄浦江入江；运河水量由西入望亭，南出盛泽；原出海的“三江”，今由黄浦江东泄入江，由此形成苏州市的三大水系。</p>
                <p>由于苏州城内河道纵横，又称为水都、水城、水乡，十三世纪的《马可·波罗游记》将苏州赞誉为东方威尼斯。苏州古城被法国启蒙思想家孟德斯鸠称赞为“鬼斧神工”。</p>
